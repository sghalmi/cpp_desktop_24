function displayPets1(){
    var pet_1 = document.getElementById('pet1');
    var displaySetting = pet_1.style.display;

    if (displaySetting == "none"){
        pet_1.style.display = "block";
    }
    else{
        pet_1.style.display = "none";
    }
}
