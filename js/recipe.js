function displayRecipes1(){
    var rec_1 = document.getElementById('recipe1');
    var displaySetting = rec_1.style.display;

    if (displaySetting == "none"){
        rec_1.style.display = "block";
    }
    else{
        rec_1.style.display = "none";
    }
}

